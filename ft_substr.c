#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char		*sub;
	size_t		r;
	size_t		i;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		r = 0;
	else
		r = ft_strlen(s) - start;
	if (r < len)
		len = r;
	sub = malloc(sizeof(*sub) * (len + 1));
	if (!sub)
		return (NULL);
	i = 0;
	while (s[i] && i < len)
	{
		sub[i] = s[i + start];
		i++;
	}
	sub[i] = '\0';
	return (sub);
}
